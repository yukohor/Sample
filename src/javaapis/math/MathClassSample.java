package javaapis.math;

public class MathClassSample {

public static void main(String [] args) {
    double flr = Math.floor(12.81);
    double rnd = Math.round(12.81);
    double cl = Math.ceil(12.81);
    double pow = Math.pow(9, 2);
    double sqrt = Math.sqrt(144);
    double rad = 10 * 10 * Math.PI;
    
    System.out.println(flr);
    System.out.println(rnd);
    System.out.println(cl);
    System.out.println(pow);
    System.out.println(sqrt);
    System.out.println(rad);
}
}
    
